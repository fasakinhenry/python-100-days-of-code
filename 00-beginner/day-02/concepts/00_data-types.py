# Primitive Data types

# String
print("Hello"[0])
# numbers within quotation marks are treated as strings not numbers
print("123" + "345") #concatenates instead of add

# Integers
# Integer is a programming lingo for whole numbers(numbers without decimal places)
print(123 + 345)
# You can seperate large integers with underscore e.g
print(123_456_789)

# Float
# Float is short for floating-point numbers(decimal numbers). E.g
float_num = 3.14159 # PI is a floating point number
print(type(float_num))

# Boolean
# This data type has only two possible values(True or False)
checked = True
print(type(checked)) # <class 'bool'>

